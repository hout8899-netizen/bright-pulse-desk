export type TKey = keyof (typeof dict)["en"];

export const LANGUAGES: { code: Lang; label: string; native: string }[] = [
  { code: "en", label: "English", native: "English" },
  { code: "km", label: "Khmer", native: "бһҒбҹ’бһҳбҹӮбһҡ" },
  { code: "zh", label: "Chinese", native: "дёӯж–Ү" },
];

type Ctx = { lang: Lang; setLang: (l: Lang) => void; t: (k: TKey) => string };
const I18nContext = createContext<Ctx | null>(null);

function readInitial(): Lang {
  if (typeof window === "undefined") return "en";
  const v = window.localStorage.getItem(STORAGE_KEY);
  if (v === "en" || v === "km" || v === "zh") return v;
  return "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Lang>("en");

  useEffect(() => {
    setLangState(readInitial());
  }, []);

  useEffect(() => {
    if (typeof document !== "undefined") {
      document.documentElement.lang = lang;
    }
  }, [lang]);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    if (typeof window !== "undefined") window.localStorage.setItem(STORAGE_KEY, l);
  }, []);

  const t = useCallback((k: TKey) => (dict[lang] as Record<string, string>)[k] ?? dict.en[k] ?? k, [lang]);

  const value = useMemo(() => ({ lang, setLang, t }), [lang, setLang, t]);
  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const ctx = useContext(I18nContext);
  if (!ctx) throw new Error("useI18n must be used inside I18nProvider");
  return ctx;
}

/** Translate a Status value to current language. */
export function useStatusLabel() {
  const { t } = useI18n();
  return (s: string) => {
    if (s === "Completed") return t("status.Completed");
    if (s === "In Progress") return t("status.InProgress");
    if (s === "Pending") return t("status.Pending");
    if (s === "Overdue") return t("status.Overdue");
    return s;
  };
}

/** Translate a Priority value to current language. */
export function usePriorityLabel() {
  const { t } = useI18n();
  return (p: string) => {
    if (p === "High") return t("priority.High");
    if (p === "Medium") return t("priority.Medium");
    if (p === "Low") return t("priority.Low");
    return p;
  };
}
